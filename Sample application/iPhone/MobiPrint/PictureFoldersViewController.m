- (void)viewDidLoad
{
    id appDelegate = (id)[[UIApplication sharedApplication] delegate];
    self.managedObjectContext = [appDelegate managedObjectContext];
    
    NSManagedObject *newPictureFolder = [NSEntityDescription insertNewObjectForEntityForName:@"PictureFolder" inManagedObjectContext:self.managedObjectContext];
    
    [newPictureFolder setValue:@"Barcelona" forKey:@"name"];
    [newPictureFolder setValue:@"/dontknow" forKey:@"path"];
    [newPictureFolder setValue:[NSNumber numberWithInt:55] forKey:@"numPictures"];
    
    NSError *error;
    if(![self.managedObjectContext save:&error])
        NSLog(@"Failed to save new picture folder: %@", [error localizedDescription]);
    
    [super viewDidLoad];
    
    [self controllerDidChangeContent:nil];
}

